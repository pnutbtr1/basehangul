int hg_getidx(int hgch)
{
    int i;
    uint8_t firstByte;

    hgch -= 0xac00;
    firstByte = (hgch >> 8) & 0xFF;
    if (firstByte >= HAN_IDX_LEN)
        return -1; // out of range
    for (i = han_idx[firstByte]; i < han_idx[firstByte + 1]; i++)
        if (han2350[i] == hgch)
            return i;
    return -1; // chacter not is KS X 1001
}

